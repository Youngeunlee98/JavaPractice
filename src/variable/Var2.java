package variable;

public class Var2 {
    public static void main(String[] args) {
        int a; // 변수 선언
        a = 10; //변수 초기화

        System.out.println(a); // -> a 에 담겨있는 10을 출력하게 된다.

        a = 15; //변수를 10 에서 15로 변경한다면?
        System.out.println(a); // -> 변경된 15로 출력된다.


        //변수는 변하는 수이다. 언제든지 중간에 바꿀수 있다.
    }
}
