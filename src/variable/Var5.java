package variable;

public class Var5 {
    public static void main(String[] args) {
        //변수의 타입
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언(boolean) : true,false 입력 가능
        char d = 'A'; // 문자 하나
        String e = "Hello world"; // 문자열, 문자열을 다루기 위한 특별한 타입

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        //각 변수는 지정한 타입에 맞는 값을 사용해야 하고 그렇지 않으면 컴파일 오류가 발생한다.
        //리터럴 -> 코드엣서 개발자가 직접 적은 고정된 값을 리터럴 이라고 한다.
    }
}
